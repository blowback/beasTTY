// Expand the 96-char × 8-row source to the 128-char × 16-row footprint shared
// by every font in the registry. Codepoints 0..31 stay all-zero (blank). Each
// source row is emitted twice — same chunky-scanline scaling as VT52 ROM.
function expandInsigbyteToRendererFootprint(raw) {
    const out = new Uint8Array(128 * 16);
    for (let i = 0; i < 96; i++) {
        const ch = i + 32;
        for (let r = 0; r < 8; r++) {
            const src = raw[i * 8 + r];
            out[ch * 16 + r * 2 + 0] = src;
            out[ch * 16 + r * 2 + 1] = src;
        }
    }
    return out;
}

export const INSIGBYTE_FONT = expandInsigbyteToRendererFootprint(RAW_INSIGBYTE);
